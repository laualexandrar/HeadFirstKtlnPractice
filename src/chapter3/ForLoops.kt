package chapter3

fun main() {
    for (x in 1..30) print ("$x ")
    println("\n")
    for(y in 1 until 30) print("$y ")
    println("\n")
    for(f in 1..10) print ("$f ")
println("\n")
    for(h in 23 downTo 5) print("$h ")
    println("\n")
    for(g in 34 downTo 2) print("$g ")
    println("\n")
    for(k in 1..40 step 3) print("$k ")
}
